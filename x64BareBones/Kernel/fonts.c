font_char_p getCharBitMap(char c)
{
    if (c < FIRST_PRINTABLE_CHAR || c > LAST_PRINTABLE_CHAR)
        return NULL;

    // Cast explícito para convertir font_char_t a puntero simple
    return (font_char_p)char_fonts[c - FIRST_PRINTABLE_CHAR];
}