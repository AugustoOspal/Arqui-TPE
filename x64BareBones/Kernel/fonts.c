const static int font_heights[3] = {
    FONT_CHAR_HEIGHT_LARGE_BYTES,
    FONT_CHAR_HEIGHT_MEDIUM_BYTES,
    FONT_CHAR_HEIGHT_BYTES
};

const static int font_widths[3] = {
    FONT_CHAR_WIDTH_LARGE_BYTES, 
    FONT_CHAR_WIDTH_MEDIUM_BYTES , 
    FONT_CHAR_WIDTH_BYTES
};

const static uint32_t font_bit_masks[3] = {
    FONT_MASK_LARGE, 
    FONT_MASK_MEDIUM, 
    FONT_MASK_SMALL
};

FontChar getCharBitMap(char c)
{
    FontChar result = {NULL, 0, 0, 0};

    if (c < FIRST_PRINTABLE_CHAR || c > LAST_PRINTABLE_CHAR)
        return result;

    int idx = c - FIRST_PRINTABLE_CHAR;

    switch (currentFont) {
        case 0:
            result.bitmap = (const void*)&font_large[idx];
            result.height = font_heights[0];
            result.width = font_widths[0];
            result.bitMask = font_bit_masks[0];
            break;
        case 1:
            result.bitmap = (const void*)&font_medium[idx];
            result.height = font_heights[1];
            result.width = font_widths[1];
            result.bitMask = font_bit_masks[1];
            break;
        case 2:
            result.bitmap = (const void*)&font_small[idx];
            result.height = font_heights[2];
            result.width = font_widths[2];
            result.bitMask = font_bit_masks[2];
            break;
    }
    
    return result;
}

int getWidth(){
    return font_widths[currentFont];
}

int getHeight(){
    return font_heights[currentFont];
}
